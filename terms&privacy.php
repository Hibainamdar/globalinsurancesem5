        <h2>Terms and Policy</h2>
    Guidelines for policy holders
We welcome you as a policyholder and as a prospective customer to our customer service section.
 This section will guide you through the various intricacies of a life insurance contract and the 
 facts that you must know to make the best out of your life insurance policy.Please read our 
 guidelines immediately.<br>
 <b>Payment of Premiums:</b>
A grace period of one month but not less than 30 days is allowed where the mode of payment is 
yearly, half-yearly or quarterly and 15 days for monthly payments. If death occurs within this
 period, the life assured is covered for full sum assured.<br>
<b>Non-forfeiture regulations: </b>
If the policy has run for atleast 3 full years and subsequent premiums have not been paid 
 policy shall not be void but the sum assured will be reduced to a sum which will bear the same
  ratio as to the number of premiums paid bear to the total number of premiums payable.The 
  concessions regarding claim in the above case is explained in the appropriate section.<br>
<b>Forfeiture in certain events:</b>
In case of untrue or incorrect statement contained in the proposal, personal statement, declaration
 and connected documents or any material information with held, subject to the provision of Section
  45 of the Insurance Act 1938, wherever applicable, the policy shall be declared void and all 
  laims to any benefits in virtue thereof shall cease.<br>
<b>Suicide:</b>
The policy shall be void, if the Life Assured commits suicide (whether sane or insane at the time)
 at any time or after the date on which the risk under the policy has commenced but before the 
 expiry of one year from the date of commencement of the policy.<br>
<b>Guaranteed Surrender Value:</b> 
After payment of premiums for at least three years, the Surrender Value allowed under the policy 
is equal to 30% of the total premiums paid excluding premiums for the 1st year and all extra 
premiums.<br>
<b>Salary Saving Scheme:</b>
The rate of installment premium shown in the schedule of the policy will remain constant as long 
as the employee continues with the employer given in the proposal. On leaving the employment of 
said employer the policyholder should intimate the Corporation. In case of the Salary Saving 
Scheme being withdrawn by the said employer, the Corporation will intimate the same to the 
policyholder. Thereafter the 5% rebate given under Salary Saving Scheme will be withdraw.<br>
<b>Exclusion in accidental death</b>
Accidental death is included in the term insurance plan, but that does not mean the claim made 
under such a scenario will always be accepted. In case of accidental deaths, insurance companies 
always investigate the circumstances of the incident. The procedure of such an investigation might 
vary from insurer to insurer, but the findings must be satisfactory for the claim to be accepted.<br>
